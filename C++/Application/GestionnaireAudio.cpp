#include "GestionnaireAudio.h"


SINGLETON_DECLARATION_CPP(GestionnaireAudio);

////////////////////////////////////////////////////////////////////////
///
/// @fn GestionnaireAudio::GestionnaireAudio()
///
/// Constructeur par defaut, initialise le systeme de son
///
/// @return aucun.
///
//////////////////////////////////////////////////////////////////////// 
GestionnaireAudio::GestionnaireAudio()
{
	FMOD::System_Create(&systeme);
	systeme->init(7, FMOD_INIT_NORMAL, NULL); // 7 channels


}

////////////////////////////////////////////////////////////////////////
///
/// @fn void GestionnaireAudio::~GestionnaireAudio();
///
/// destructeur, libere le systeme de son
///
/// @return aucun.
///
//////////////////////////////////////////////////////////////////////// 
GestionnaireAudio::~GestionnaireAudio()
{
	
	audioActivationBonusAccelerateur->release() ;
	audioCollision->release();
	audioDestructionAsteroide->release();
	audioDestructionStationSpatiale->release();
	audioTirProjectile->release();
	audioUtilisationPortail->release();
	
	systeme->release();
	systeme->close();
}

////////////////////////////////////////////////////////////////////////
///
/// @fn void GestionnaireAudio::
///
/// Cette fonction
///
/// @param[in]   :
///
/// @return aucun.
///
////////////////////////////////////////////////////////////////////////

void GestionnaireAudio::jouerAudioActivationBonusAccelerateur()
{
	systeme->createSound("media/audio/mp3/audioActivationBonusAccelerateur.mp3", FMOD_CREATESAMPLE, 0, &audioActivationBonusAccelerateur);
	systeme->playSound( FMOD_CHANNEL_FREE, audioActivationBonusAccelerateur, 0, NULL);
	systeme->update();
}
